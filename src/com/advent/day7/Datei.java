package com.advent.day7;

public class Datei {
    public String name;
    public int groessse;

    public Datei(String name, int groessse) {
        super();
        this.name = name;
        this.groessse = groessse;
    }

    public String getName() {
        return name;
    }

    public int getGroessse() {
        return groessse;
    }

}
